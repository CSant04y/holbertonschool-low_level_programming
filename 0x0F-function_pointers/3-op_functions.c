/**
 * op_add - this function adds two ints
 * @a: this is int a
 * @b: this is int b
 * Return: this returns the sum of a and b
 *
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - This function subtracts two ints
 * @a: this is int a
 * @b: this is int b
 * Return: is diffrence of a and b
 *
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - This function multiplies two ints
 * @a: This is int a
 * @b: This is int b
 * Return: this is the product between a and b
 *
 */
int op_mul(int a, int b)
{
	return (a * b);
}
